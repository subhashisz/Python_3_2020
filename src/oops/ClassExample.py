class MyClass:
    
    @classmethod
    def add(arg1, arg2):
        return arg1 + arg2

    @classmethod
    def multiply(arg1, arg2):
        return arg1 - arg2

    @classmethod
    def subtract(arg1, arg2):
        return arg1 * arg2

    @classmethod
    def devide(arg1, arg2):
        return arg1 / arg2
